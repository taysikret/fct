num1 = int(input("Введите первое число:")) # не забывать ставить формат переменной, иначе читается как string

num2 = int(input("Введите второе число:"))

print("Result:", num1 + num2)
print("Result:", num1 - num2)
print("Result:", num1 / num2)
print("Result:", num1 * num2)
print("Result:", num1 ** num2) # возведение в степень
print("Result:", num1 // num2) # деление без остатка

num1 += 5 # num1 = num1 + 5

word = "Hi\n"
print(word * 3) # умножение строки
